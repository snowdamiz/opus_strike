import type { CSSProperties } from 'react';

interface WalletLogoWallet {
  id?: string;
  name: string;
}

interface WalletProviderLogoProps {
  wallet: WalletLogoWallet;
  className?: string;
  style?: CSSProperties;
}

type WalletLogoKind = 'phantom' | 'solflare' | 'brave' | 'backpack' | 'solana';

function getWalletLogoKind(wallet: WalletLogoWallet): WalletLogoKind {
  const key = `${wallet.id ?? ''} ${wallet.name}`.toLowerCase();
  if (key.includes('phantom')) return 'phantom';
  if (key.includes('solflare')) return 'solflare';
  if (key.includes('brave')) return 'brave';
  if (key.includes('backpack')) return 'backpack';
  return 'solana';
}

export function WalletProviderLogo({ wallet, className, style }: WalletProviderLogoProps) {
  switch (getWalletLogoKind(wallet)) {
    case 'phantom':
      return <PhantomWalletLogo className={className} style={style} />;
    case 'solflare':
      return <SolflareWalletLogo className={className} style={style} />;
    case 'brave':
      return <BraveWalletLogo className={className} style={style} />;
    case 'backpack':
      return <BackpackWalletLogo className={className} style={style} />;
    case 'solana':
    default:
      return <SolanaWalletLogo className={className} style={style} />;
  }
}

